#include <stdio.h>
int a[3],b[3],r[3];
int
main() {
  scanf("%d:%d:%d",a,a+1,a+2);
  scanf("%d:%d:%d",b,b+1,b+2);
  r[0]=(b[0]-a[0])%24+(b[0]-a[0]<0?24:0);
  r[1]=(b[1]-a[1]);
  r[2]=(b[2]-a[2]);
  if(r[2]<0)r[1]--,r[2]+=60;
  else if(r[2]>=60)r[1]++,r[2]-=60;
  if(r[1]<0)r[0]--,r[1]+=60;
  else if(r[1]>=60)r[0]++,r[1]-=60;
  if(a[0]==b[0]&&a[1]==b[1]&&a[2]==b[2])r[0]=24,r[1]=0,r[2]=0;
  printf("%02d:%02d:%02d\n",r[0],r[1],r[2]);
}
